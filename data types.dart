void main() {
  // Integer is a number without a decimal point
  int age = 30;
  print('Integer: $age');

  // Double is a number with a decimal point
  double pi = 3.14;
  print('Double: $pi');

  // String is a sequence of characters
  String name = 'John Doe';
  print('String: $name');

  // List is an ordered group of items
  List<int> numbers = [1, 2, 3, 4, 5];
  print('List: $numbers');

  // Map is an unordered collection of key-value pairs
  Map<String, int> fruitCounts = {
    'apples': 10,
    'oranges': 5,
    'bananas': 20,
  };
  print('Map: $fruitCounts');
}
